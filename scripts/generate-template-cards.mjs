#!/usr/bin/env node
import {readFileSync, writeFileSync, mkdirSync} from 'node:fs'
import {dirname, resolve} from 'node:path'
import {fileURLToPath} from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')

const STACK_ICONS = [
  ['react-router', 'react'],
  ['react', 'react'],
  ['preact', 'react'],
  ['vue', 'vuejs'],
  ['svelte', 'bolt-lightning'],
  ['typescript', 'typescript'],
  ['chatgpt', 'robot'],
  ['shadcn', 'palette'],
  ['transformers-js', 'microchip-ai'],
  ['tailwindcss', 'wind'],
  ['turbopack', 'bolt'],
  ['playwright', 'bug-slash'],
  ['crypto', 'key'],
  ['monorepo', 'diagram-project'],
  ['locales', 'language'],
  ['less', 'droplet'],
  ['sass', 'droplet'],
  ['css-modules', 'cube'],
  ['env', 'gear'],
  ['main-world', 'globe'],
  ['browser-flags', 'flag'],
  ['multi-', 'layer-group'],
  ['custom-font', 'font'],
  ['config-eslint', 'shield-check'],
  ['config-prettier', 'wand-magic-sparkles'],
  ['config-stylelint', 'paint-roller'],
  ['special-folders-pages', 'folder-tree'],
  ['special-folders-scripts', 'folder-tree'],
  ['sidebar', 'bars'],
  ['action', 'bullseye'],
  ['content', 'file-code'],
  ['new', 'window-maximize'],
  ['init', 'rocket'],
  ['javascript', 'js']
]

const CONTEXT_LABEL = {
  new: 'New tab page',
  content: 'Content script',
  action: 'Toolbar popup',
  sidebar: 'Side panel',
  'special-folders': 'Special folders',
  init: 'Default baseline',
  javascript: 'Baseline',
  typescript: 'Baseline',
  playwright: 'E2E testing',
  react: 'Baseline',
  preact: 'Baseline',
  vue: 'Baseline',
  svelte: 'Baseline'
}

function pickIcon(slug) {
  for (const [needle, icon] of STACK_ICONS) {
    if (slug.includes(needle)) return icon
  }
  return 'puzzle'
}

function pickContextLabel(slug) {
  if (slug === 'init' || slug === 'new') return 'Default baseline'
  for (const prefix of [
    'new-',
    'content-',
    'action-',
    'sidebar-',
    'special-folders-'
  ]) {
    if (slug.startsWith(prefix)) return CONTEXT_LABEL[prefix.replace(/-$/, '')]
  }
  return CONTEXT_LABEL[slug] || 'Example'
}

function formatTitle(slug) {
  return slug
    .split('-')
    .map((w) => w[0].toUpperCase() + w.slice(1))
    .join(' ')
}

async function main() {
  const api =
    'https://api.github.com/repos/extension-js/examples/contents/examples'
  const res = await fetch(api, {
    headers: {'User-Agent': 'extensionjs-docs-build'}
  })
  if (!res.ok) {
    throw new Error(`GitHub API error ${res.status}: ${await res.text()}`)
  }
  const entries = await res.json()
  const slugs = entries
    .filter((e) => e.type === 'dir')
    .map((e) => e.name)
    .sort()

  const cards = slugs
    .map((slug) => {
      const title = formatTitle(slug)
      const context = pickContextLabel(slug)
      const icon = pickIcon(slug)
      const href = `https://github.com/extension-js/examples/tree/main/examples/${slug}`
      return `  <Card title="${title}" icon="${icon}" href="${href}">\n    ${context} · \`--template=${slug}\`\n  </Card>`
    })
    .join('\n')

  const snippet = `{/* Auto-generated by scripts/generate-template-cards.mjs — do not edit by hand. */}\n\n<CardGroup cols={2}>\n${cards}\n</CardGroup>\n`

  mkdirSync(resolve(ROOT, 'snippets'), {recursive: true})
  writeFileSync(resolve(ROOT, 'snippets/template-cards.mdx'), snippet)
  console.log(`wrote snippets/template-cards.mdx (${slugs.length} templates)`)

  // Inject into docs/getting-started/templates.mdx between markers
  const target = resolve(ROOT, 'docs/getting-started/templates.mdx')
  const current = readFileSync(target, 'utf8')
  const BEGIN = '{/* <TEMPLATE_CARDS_BEGIN> */}'
  const END = '{/* <TEMPLATE_CARDS_END> */}'
  const cardBlock = `${BEGIN}\n\n${snippet}\n${END}`
  let updated
  if (current.includes(BEGIN) && current.includes(END)) {
    updated = current.replace(
      new RegExp(
        `${BEGIN.replace(/[/*]/g, '\\$&')}[\\s\\S]*?${END.replace(/[/*]/g, '\\$&')}`
      ),
      cardBlock
    )
  } else {
    // Replace the <div>...<TemplateCardsGrid />...</div> block or bare <TemplateCardsGrid />
    updated = current
      .replace(/<div[^>]*>\s*<TemplateCardsGrid\s*\/>\s*<\/div>/, cardBlock)
      .replace(/<TemplateCardsGrid\s*\/>/, cardBlock)
  }
  writeFileSync(target, updated)
  console.log(`updated docs/getting-started/templates.mdx`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
